
// switch, case, break, default

var digit=("Enter a digit: ");
document.write(typeof(digit)+"<br>");

switch(digit){

    case "0":
        document.write("zero");
        break;

    case "1":
        document.write("one");
        break;

    case "2":
        document.write("two");
        break;

    case "3":
        document.write("three");
        break;

    case "4":
        document.write("four");
        break;

    case "5":
        document.write("five");
        break;

    case "6":
        document.write("six");
        break;

    case "7":
        document.write("seven");
        break;

    case "8":
        document.write("eight");
        break;

    case "9":
        document.write("nine");
        break;

    default:
        document.write("not digit");



}
